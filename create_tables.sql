DROP TABLE faers_rpsr;
CREATE TABLE faers_rpsr (
    PRIMARYID VARCHAR(255) NOT NULL,
    CASEID VARCHAR(255) NOT NULL,
    RPSR_COD VARCHAR(255) NOT NULL,
    PRIMARY KEY (PRIMARYID)
